#include "fdf.h"

void	ft_mtrxdel(t_matrix **mat)
{
	size_t	r;

	r = -1;
	while (++r < (*mat)->rows)
		free((*mat)->tab[r]);
	free((*mat)->tab);
	free(*mat);
	*mat = NULL;
}
